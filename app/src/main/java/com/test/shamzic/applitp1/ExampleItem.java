package com.test.shamzic.applitp1;

public class ExampleItem {

    private String mImageUrl;
    private String mWebsiteName;
    private String mWebsiteURL;

    public ExampleItem (String mImageUrl, String mWebsiteName, String mWebsiteURL) {
        this.mImageUrl = mImageUrl;
        this. mWebsiteName = mWebsiteName;
        this.mWebsiteURL = mWebsiteURL;
    }

    public String getmImageUrl() { return mImageUrl; }

    public String getmWebsiteName() { return mWebsiteName; }

    public String getmWebsiteURL() { return mWebsiteURL; }


}
